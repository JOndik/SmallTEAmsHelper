namespace BPAddIn
{
    public class LogInService
    {
        //private const string serviceAddress = "http://localhost:8080";
        //private const string serviceAddress = "http://147.175.180.200:8080";
        private const string serviceAddress = "http://192.168.1.138:8080";
        //private const string serviceAddress = "https://ichiban.fiit.stuba.sk:8443";

        public string checkConnection(string name, string password)
        {
            try
            {
                //System.Net.ServicePointManager.SecurityProtocol = System.Net.SecurityProtocolType.Tls12;
                //System.Net.ServicePointManager.ServerCertificateValidationCallback = delegate { return true; };

                using (WebClient webClient = new WebClient())
                {
                    /*System.Net.ServicePointManager.SecurityProtocol = System.Net.SecurityProtocolType.Tls12;
                    System.Net.ServicePointManager.ServerCertificateValidationCallback = delegate { return true; };*/
                    
                    using (var stream = webClient.OpenRead(serviceAddress))
                    {
                        stream.Close();
                        return this.uploadLogInData(name, password);
                    }
                }
            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.ToString());
                return "noconnection";
            }           
        }


        public string uploadLogInData(string name, string password)
        {
            LogIn logIn = new LogIn(name, password);



            using (WebClient webClient = new WebClient())
            {
                string result = "";
                string data = "";            
               

                webClient.Headers[HttpRequestHeader.ContentType] = "application/json; charset=utf-8";
                //data = Encoding.UTF8.GetString(Encoding.Unicode.GetBytes(dtoWrapper.serialize()));
                data = EncodeNonAsciiCharacters(logIn.serialize());
                result = webClient.UploadString(serviceAddress + "/auth", data);

                if (("false").Equals(result) || ("error").Equals(result))
                {                   
                    return result;
                }
                else
                {
                    saveUserToLocalDatabase(logIn.name, result);
                    return result;
                }
            }
        }

        public void saveUserToLocalDatabase(String name, String token)
        {
            List<User> users;

            lock (LocalDBContext.Lock)
            {
                using (LocalDBContext context = new LocalDBContext())
                {
                    users = context.user.ToList();
                    User user = new User();
                    
                    if (users.Count > 0)
                    {
                        user = users.First();
                        user.username = name;
                        user.token = token;
                        context.SaveChanges();
                    }
                    else
                    {                    
                        user.id = 1;
                        user.username = name;
                        user.token = token;
                        context.user.Add(user);
                        context.SaveChanges();
                    }

                    ChangeService.userToken = user.token;
                }
            }           
        }

        static string EncodeNonAsciiCharacters(string value)
        {
            StringBuilder sb = new StringBuilder();
            foreach (char c in value)
            {
                if (c > 127)
                {
                    string encodedValue = "\\u" + ((int)c).ToString("x4");
                    sb.Append(encodedValue);
                }
                else
                {
                    sb.Append(c);
                }
            }

            return sb.ToString();
        }
    }
}
